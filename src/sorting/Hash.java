package sorting;

import java.util.HashMap;

public class Hash {
    public static void main(String[] args) {
        HashMap<Integer,String> hm = new HashMap<>();

        //put value in hashmap
        hm.put(1, "Aman");
        hm.put(2,"Tousif");
        hm.put(3,"Saif");
        hm.put(4,"Salil");

        System.out.println(hm.get(1));

    }
}
